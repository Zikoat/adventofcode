function p1(input: string): number {
  const areas: number[][] = [];
  const map = input.split("\n").map((row) => row.split(""));

  let maxId = -1;
  const scores: { area: number; perimeter: number; name: string }[] = [];

  function floodFill(x: number, y: number, cell: string): number {
    // console.log(x, y, cell);
    let id: number | undefined = areas[y]?.[x];

    if (id === undefined) {
      if (map[y - 1]?.[x] === cell && typeof areas[y - 1]?.[x] === "number") {
        id = areas[y - 1]?.[x];
      }
      if (map[y]?.[x - 1] === cell && typeof areas[y]?.[x - 1] === "number") {
        id = areas[y]?.[x - 1];
      }
      if (map[y + 1]?.[x] === cell && typeof areas[y + 1]?.[x] === "number") {
        id = areas[y + 1]?.[x];
      }
      if (map[y]?.[x + 1] === cell && typeof areas[y]?.[x + 1] === "number") {
        id = areas[y]?.[x + 1];
      }
      if (id === undefined) {
        maxId++;
        id = maxId;
        // console.log("creating new id", id);
        // console.table(areas);
      }
      if (areas[y] === undefined) areas[y] = [];
      areas[y][x] = id;
    }

    if (map[y - 1]?.[x] === cell && !(typeof areas[y - 1]?.[x] === "number")) {
      // console.log("floodfill continue");
      floodFill(x, y - 1, cell);
    }
    if (map[y]?.[x - 1] === cell && !(typeof areas[y]?.[x - 1] === "number")) {
      // console.log("floodfill continue");
      floodFill(x - 1, y, cell);
    }
    if (map[y + 1]?.[x] === cell && !(typeof areas[y + 1]?.[x] === "number")) {
      // console.log("floodfill continue");
      floodFill(x, y + 1, cell);
    }
    if (map[y]?.[x + 1] === cell && !(typeof areas[y]?.[x + 1] === "number")) {
      // console.log("floodfill continue");
      floodFill(x + 1, y, cell);
    }

    return id;
  }

  for (let y = 0; y < map.length; y++) {
    const row = map[y];
    ass(row);
    for (let x = 0; x < row.length; x++) {
      const cell = row[x];
      ass(cell);
      // console.log("starting floodfill at", x, y, cell);
      const id = floodFill(x, y, cell);

      let score = scores[id];
      if (!score) {
        score = { area: 0, perimeter: 0, name: cell };
        scores[id] = score;
      }
      // console.log(
      //   "increasing score of",
      //   cell,
      //   "with id",
      //   id,
      //   "to",
      //   score.area + 1
      // );
      score.area = score.area + 1;

      if (map[y + 1]?.[x] !== cell) score.perimeter++;
      if (map[y - 1]?.[x] !== cell) score.perimeter++;
      if (map[y]?.[x + 1] !== cell) score.perimeter++;
      if (map[y]?.[x - 1] !== cell) score.perimeter++;

      let areasRow = areas[y];
      if (areasRow === undefined) {
        areasRow = [];
        areas[y] = areasRow;
      }
      areasRow[x] = id;
    }
  }
  // console.log(scores);
  let score = 0;
  for (const scoreEntry of scores) {
    score += scoreEntry.area * scoreEntry.perimeter;
  }
  return score;
}
